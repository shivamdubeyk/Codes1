//code for typecasting.
#include<iostream>
using namespace std;
int main()
{
    int a = 'a';
    char b = 99;
    float f = 1;
    int d = 1.23;
    
    cout<< "value of (int a = 'a') is ." << a <<endl;
    cout<< "value of (char b = 99) is ." << b <<endl;
    cout<< "value of (float f = 1) is ." << f <<endl;
    cout<< "value of (int d =1.23) is ." << d <<endl;

}